def report_full_period(df: pd.DataFrame, group_name: str, individual=False):
    """Полный отчет за весь период обучения"""
    report_type = 'Весь период'
    report_dir = ensure_report_directory(group_name, report_type)

    if individual:
        # Индивидуальные отчеты по студентам
        students = [col for col in df.columns if col not in ['Дисциплина', 'Тип', 'Семестр']]
        for student in students:
            student_data = df[['Семестр', 'Дисциплина', 'Тип', student]].copy()
            student_data = student_data.rename(columns={student: 'Оценка'})
            output_file = os.path.join(report_dir, f'{student}_весь_период_{date_now}.xlsx')
            df_to_excel_autoresize(student_data, output_file, individual=individual)
        print(f"Полные индивидуальные отчеты сохранены в: {os.path.join(group_name, report_dir)}")

    else:
        # Общий отчет по группе — с добавлением среднего балла
        df_with_avg = df.copy()

        # Определяем колонки студентов
        student_cols = [col for col in df.columns if col not in ['Дисциплина', 'Тип', 'Семестр']]

        # Считаем средний балл по каждому студенту
        avg_row = {}
        avg_row['Дисциплина'] = 'Средний балл'
        avg_row['Тип'] = ''
        avg_row['Семестр'] = ''

        for student in student_cols:
            scores = df[student].apply(grade_to_score)
            avg_score = scores.mean()
            # Округляем до 2 знаков (например, 4.33)
            avg_row[student] = round(avg_score, 2)

        # Добавляем строку в конец
        df_with_avg = pd.concat([df_with_avg, pd.DataFrame([avg_row])], ignore_index=True)

        output_file = os.path.join(report_dir, f'весь_период_группа_{group_name}_{date_now}.xlsx')
        df_to_excel_autoresize(df_with_avg, output_file, individual=individual)

        print(f"Полный общий отчет сохранен в: {os.path.join(group_name, report_dir)}")

