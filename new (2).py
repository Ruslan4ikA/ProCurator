def ensure_report_directory(group_name, report_type):
    """Создание папок для отчетов"""
    base_dir = 'Report'  # Базовая папка со всеми отчетами
    group_dir = os.path.join(base_dir, group_name)  # Папка с отчетами по группе
    report_dir = os.path.join(group_dir, report_type)  # Папка отчетов по типу

    if not os.path.exists(base_dir):
        os.makedirs(base_dir)
    if not os.path.exists(group_dir):
        os.makedirs(group_dir)
    if not os.path.exists(report_dir):
        os.makedirs(report_dir)

    return report_dir

