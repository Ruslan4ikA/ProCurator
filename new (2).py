def grade_to_score(grade: str) -> int:
    """Преобразует текстовую оценку в числовой балл (2–5)"""
    g = str(grade).strip().lower()
    if g in {'отл.', 'зач.'}:
        return 5
    elif g in {'хор.'}:
        return 4
    elif g in {'уд.'}:
        return 3
    elif g in {'н/з', 'неуд.', 'н/а', ''}:
        return 2
    else:
        # На случай неизвестной оценки — считаем долгом
        return 2

def is_success(grade: str) -> bool:
    """Абсолютная успеваемость: оценка ≥ 3 (включая зачёты)"""
    return grade_to_score(grade) >= 3

def is_quality(grade: str, assessment_type: str) -> bool:
    """Качественная успеваемость: '4' или '5', НЕ включая зачёты"""
    g = str(grade).strip().lower()
    # Только экзамены и зачёты с оценкой
    if 'зачет с оценкой' in assessment_type.lower():
      return g in {'отл.', 'хор.', 'отлично', 'хорошо'}
    elif 'зачет' in assessment_type.lower() or 'зач.' in g:
        return False
    return g in {'отл.', 'хор.', 'отлично', 'хорошо'}

from matplotlib.ticker import PercentFormatter
def generate_plots(df: pd.DataFrame, group_name: str, output_dir: str):
    """Построение всех 6 графиков по данным успеваемости"""
    os.makedirs(output_dir, exist_ok=True)

    # Извлекаем номер семестра из строки вида "1 семестр"
    def extract_semester(semester_str):
        match = re.search(r'(\d+)', str(semester_str))
        return int(match.group(1)) if match else None

    df = df.copy()
    df['sem_num'] = df['Семестр'].apply(extract_semester)
    df = df.dropna(subset=['sem_num']).copy()
    df['sem_num'] = df['sem_num'].astype(int)

    student_cols = [col for col in df.columns if col not in ['Дисциплина', 'Тип', 'Семестр', 'sem_num']]

    # === 1. Абсолютная и качественная успеваемость по семестрам ===
    semesters = sorted(df['sem_num'].unique())
    au_list, kz_list = [], []

    for sem in semesters:
        sem_df = df[df['sem_num'] == sem]
        total_students = len(student_cols)

        # Абсолютная успеваемость
        success_count = sem_df[student_cols].applymap(is_success).sum().sum()
        au = success_count / (total_students * len(sem_df)) * 100

        # Качественная успеваемость
        kz_count = 0
        for _, row in sem_df.iterrows():
            for student in student_cols:
                if is_quality(row[student], row['Тип']):
                    kz_count += 1
        kz = kz_count / (total_students * len(sem_df)) * 100

        au_list.append(au)
        kz_list.append(kz)

    plt.figure(figsize=(10, 6))
    plt.plot(semesters, au_list, marker='o', label='Абсолютная успеваемость')
    plt.plot(semesters, kz_list, marker='s', label='Качественная успеваемость')
    plt.title('Успеваемость по семестрам')
    plt.xlabel('Семестр')
    plt.ylabel('Проценты')
    plt.gca().yaxis.set_major_formatter(PercentFormatter())
    plt.legend()
    plt.grid(True)
    plt.savefig(os.path.join(output_dir, '1_успеваемость_по_семестрам.png'), bbox_inches='tight')
    plt.show()
    plt.close()

    # === 2. Общее количество долгов по семестрам ===
    debt_counts = []
    for sem in semesters:
        sem_df = df[df['sem_num'] == sem]
        debt_count = sem_df[student_cols].applymap(lambda x: grade_to_score(x) == 2).sum().sum()
        debt_counts.append(debt_count)

    plt.figure(figsize=(10, 6))
    plt.bar(semesters, debt_counts, color='salmon')
    plt.title('Количество долгов по семестрам')
    plt.xlabel('Семестр')
    plt.ylabel('Число долгов')
    plt.grid(axis='y')
    plt.show()
    plt.savefig(os.path.join(output_dir, '2_долги_по_семестрам.png'), bbox_inches='tight')
    plt.close()

    # === 3. Средний балл по каждому студенту за выбранный семестр (например, 5-й) ===
    selected_sem = 5
    if selected_sem in semesters:
        sem_df = df[df['sem_num'] == selected_sem]
        avg_scores = {}
        for student in student_cols:
            scores = sem_df[student].apply(grade_to_score)
            avg_scores[student] = scores.mean()

        students_short = [s.split()[0] for s in avg_scores.keys()]  # Только имена
        plt.figure(figsize=(12, 6))
        plt.bar(students_short, avg_scores.values(), color='lightgreen')
        plt.title(f'Средний балл по студентам (семестр {selected_sem})')
        plt.xlabel('Студент')
        plt.ylabel('Средний балл')
        plt.xticks(rotation=45, ha='right')
        plt.tight_layout()
        plt.show()
        plt.savefig(os.path.join(output_dir, f'3_средний_балл_семестр_{selected_sem}.png'))
        plt.close()

    # === 4. Динамика среднего балла по студентам по семестрам ===
    avg_by_sem_student = {}
    for student in student_cols:
        avg_by_sem = []
        for sem in semesters:
            sem_df = df[df['sem_num'] == sem]
            if not sem_df.empty:
                scores = sem_df[student].apply(grade_to_score)
                avg_by_sem.append(scores.mean())
            else:
                avg_by_sem.append(None)
        avg_by_sem_student[student] = avg_by_sem

    plt.figure(figsize=(12, 7))
    for student, avgs in avg_by_sem_student.items():
        plt.plot(semesters, avgs, marker='o', label=student.split()[0])
    plt.title('Динамика среднего балла по студентам')
    plt.xlabel('Семестр')
    plt.ylabel('Средний балл')
    plt.legend(bbox_to_anchor=(1.05, 1), loc='upper left')
    plt.grid(True)
    plt.tight_layout()
    plt.show()
    plt.savefig(os.path.join(output_dir, '4_динамика_среднего_балла.png'))
