from openpyxl.styles import Alignment, PatternFill
from openpyxl.utils import get_column_letter

def df_to_excel_autoresize(df: pd.DataFrame, filepath: str, highlight_debtors: bool = True, individual: bool = False):
    """
    Сохраняет DataFrame в Excel с:
    - переносом текста по словам,
    - автоподбором ширины столбцов,
    - подсветкой долгов:
        * individual=True  → подсвечивается ВСЯ СТРОКА, если в ней есть долг,
        * individual=False → подсвечиваются ТОЛЬКО ячейки с долгами.
    """
    with pd.ExcelWriter(filepath, engine='openpyxl') as writer:
        df.to_excel(writer, index=False, sheet_name='Sheet1')
        worksheet = writer.sheets['Sheet1']

        # Стили
        wrap_align = Alignment(wrap_text=True, vertical='top')
        red_fill = PatternFill(start_color="FFCCCC", end_color="FFCCCC", fill_type="solid")

        # Определяем столбцы студентов (всё, кроме служебных)
        student_cols = [col for col in df.columns if col not in {'Дисциплина', 'Тип', 'Семестр'}]
        student_col_indices = {col: i + 1 for i, col in enumerate(df.columns)}  # Excel: 1-based

        # Проходим по строкам
        for row_idx, (_, row) in enumerate(df.iterrows(), start=2):
            # Определяем, есть ли долг в строке (только среди студентов)
            has_debt_in_row = any(
                is_debtor(row[col]) for col in student_cols if col in row
            )

            for col_idx, col_name in enumerate(df.columns, start=1):
                cell = worksheet.cell(row=row_idx, column=col_idx)
                cell.alignment = wrap_align
                if highlight_debtors:
                    if individual:
                        # Подсвечиваем ВСЮ строку, если есть долг
                        if has_debt_in_row:
                            cell.fill = red_fill
                    else:
                        # Подсвечиваем ТОЛЬКО ячейку с долгом (и только в колонках студентов)
                        if col_name in student_cols and is_debtor(row[col_name]):
                            cell.fill = red_fill

        # Автоподбор ширины столбцов
        for col_idx, col_name in enumerate(df.columns, start=1):
            max_length = max(
                df[col_name].astype(str).map(len).max(),
                len(str(col_name))
            )
            adjusted_width = min(max_length + 2, 50)
            worksheet.column_dimensions[get_column_letter(col_idx)].width = adjusted_width

