def report_debtors(df: pd.DataFrame, group_name: str, last_semesters=1):
    """Улучшенный отчет по должникам"""
    report_type = 'Должники'
    report_dir = ensure_report_directory(group_name, report_type)

    # Определяем уникальные семестры
    unique_semesters = df['Семестр'].unique()
    semester_numbers = []

    for semester in unique_semesters:
        match = re.search(r'(\d+)\s+семестр', str(semester))
        if match:
            semester_numbers.append(int(match.group(1)))

    if not semester_numbers:
        print("Семестры не найдены")
        return

    # Берем последние N семестров
    target_semesters = sorted(semester_numbers)[-last_semesters:]

    debt_data = []
    students = [col for col in df.columns if col not in ['Дисциплина', 'Тип', 'Семестр']]

    for target_semester in target_semesters:
        semester_df = df[df['Семестр'].str.contains(f"{target_semester} семестр", na=False)]

        for _, row in semester_df.iterrows():
            for student in students:
                grade = str(row[student]).lower()
                if grade in ['неуд', 'н/а', 'н/з', '']:
                    debt_data.append({
                        'Студент': student,
                        'Дисциплина': row['Дисциплина'],
                        'Тип': row['Тип'],
                        'Семестр': row['Семестр'],
                        'Оценка': row[student]
                    })

    if debt_data:
        debt_df = pd.DataFrame(debt_data)
        output_file = os.path.join(report_dir, f'должники_{last_semesters}_семестр_{group_name}_{date_now}.xlsx')
        df_to_excel_autoresize(debt_df, output_file, highlight_debtors=False)
        print(f"Отчет по должникам сохранен: {output_file}")
        print(f"Найдено {len(debt_data)} долгов за последние {last_semesters} семестра(ов)")
    else:
        print(f"Должников за последние {last_semesters} семестра(ов) не найдено")

